function bulbCount(flowerslist)
{
    var listafinal = '<table style="margin: 0 auto;">';
    flowerslist = flowerslist.split(";")
    var realflowerslist = document.getElementById('calculator_combination').getElementsByTagName('label');
    var percentlist = document.getElementById('calculator_combination').getElementsByTagName('input');
    var index = 0;

    var e = document.getElementById("measure");
    var selectedmeasure = e.options[e.selectedIndex].text;

    while (index < flowerslist.length)
    {
        var flowername = flowerslist[index];
        var realflowername = realflowerslist[index].innerHTML;
        var flowercount = getFlowerCount(flowername, selectedmeasure);
        var flowerpercent = percentlist[index].value;

        var select = document.getElementById("shape").selectedIndex;
        switch(select)
        {
            case 1 :
                var calc_length = parseFloat(document.getElementById('calc_length').value);
                var calc_width = parseFloat(document.getElementById('calc_width').value);
                a = calc_length * calc_width;
                break;
            case 2 :
                var calc_length = parseFloat(document.getElementById('calc_length').value);
                var calc_width = parseFloat(document.getElementById('calc_width').value);
                a = calc_length * calc_width / 2;
                break;
            case 3 :
                var calc_radius = parseFloat(document.getElementById('calc_radius').value);
                a = Math.PI * Math.pow(calc_radius, 2);
                break;
            case 4 :
                var calc_length = parseFloat(document.getElementById('calc_length').value);
                var calc_width = parseFloat(document.getElementById('calc_width').value);
                a = calc_length * calc_width * .8;
                break;
        }

        total = a * (flowerpercent/100) * flowercount;
        listafinal += '<tr>' +
            '<td style="text-align: left;"><label class="flowername">' + realflowername + ':</label></td>' +
            '<td style="text-align: right; font-weight: bold; color: #E23C00; font-size: 13px;">' + Math.round(total) + '</td>' +
            '</tr>';
        index++;
    }
    listafinal += "</table>";
    document.getElementById("calculator_results").innerHTML = "Recommended Quantities:" + listafinal;
}

function showfield(name)
{
    if(name=='square')
    {
        document.getElementById('calculator_square1').style.display="";
        document.getElementById('calculator_square2').style.display="";
    }
    else
    if(name=='triangle')
    {
        document.getElementById('calculator_square1').style.display="";
        document.getElementById('calculator_square2').style.display="";
    }
    else
    {
        if(name=='oval')
        {
            document.getElementById('calculator_square1').style.display="";
            document.getElementById('calculator_square2').style.display="";
        }
        else
        {
            document.getElementById('calculator_square1').style.display="none";
            document.getElementById('calculator_square2').style.display="none";
        }
    }

    if(name=='circle')
        document.getElementById('calculator_circle').style.display="";
    else
        document.getElementById('calculator_circle').style.display="none";

    document.getElementById('calculator_result').style.display="";
}